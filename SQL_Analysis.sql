-- Analysis Queries

-- KPI 1: Total Patients Count
SELECT COUNT(*) AS Patients_Count
FROM patients;

-- KPI 2: Average Treatment Cost
SELECT ROUND(AVG(treatment_cost), 2) AS Avg_Treatment_Cost
From patients;


-- KPI 3: Average Age
SELECT ROUND(AVG(age), 2) AS Average_Age
FROM patients;

-- KPI 4: Average Hospital Stay (Days)
SELECT ROUND(AVG(hospital_stay_days), 2) AS Avg_Stay_Days
FROM patients;

-- KPI 5: Average Recovery Score
SELECT ROUND(AVG(recovery_score), 2) AS Avg_Recovery_Score
FROM patients;

-- Chart 1: Average Recovery Score by Department
SELECT
    department,
    ROUND(AVG(recovery_score), 2) AS Avg_Recovery_Score
FROM patients
GROUP BY department
ORDER BY Avg_Recovery_Score DESC;

-- Chart 2: Average Recovery Score by Treatment Type
SELECT
    treatment_type,
    ROUND(AVG(recovery_score), 2) AS Avg_Recovery_Score
FROM patients
GROUP BY treatment_type
ORDER BY Avg_Recovery_Score DESC;

-- Chart 3: Gender Distribution
SELECT
    gender,
    COUNT(*) AS Patients_Count
FROM patients
GROUP BY gender;

-- Chart 4: Cost Category Distribution
SELECT
    cost_category,
    COUNT(*) AS Patients_Count
FROM patients
GROUP BY cost_category;

-- Chart 5: Average Recovery Score by Age Group
SELECT
    age_group,
    ROUND(AVG(recovery_score), 2) AS Avg_Recovery_Score
FROM patients
GROUP BY age_group
ORDER BY Avg_Recovery_Score DESC;